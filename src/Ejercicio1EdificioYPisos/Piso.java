package Ejercicio1EdificioYPisos;

public class Piso {
    private int numero;
    private int departamentos;

    public Piso(int numero, int departamentos) {
        this.numero = numero;
        this.departamentos = departamentos;
    }

    public int getNumero() {
        return numero;
    }

    public int getDepartamentos() {
        return departamentos;
    }
}
